    private void btnAceptarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarRegistroActionPerformed

        String nombre = txtNombre.getText();
        String pass = txtFieldPass.getText();
        int id_pais = cbxPais.getSelectedIndex();
        int id_ciudad = cbxCiudad.getSelectedIndex() + 1;
        
        //Obtener el fk id de la ciudad
        id_ciudad = id_ciudad + id_city_rt - 1;
        
        String str_pais = (String) cbxPais.getSelectedItem();
        String str_ciudad = (String) cbxPais.getSelectedItem();
        
        if(nombre.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nombre y contrase√±a son obligatorios.");
            return;
        }
        
        if(str_pais.equals("Seleccione un pais") || str_ciudad.equals("Seleccione una ciudad")){
            JOptionPane.showMessageDialog(null, "Favor de seleeccionar un pais y ciudad");
            return;
        }
        
        
        try {
            // Cargar el driver JDBC para MySQL
            Class.forName("com.mysql.cj.jdbc.Driver");

            String url = "jdbc:mysql://localhost:3306/CORPCORP_APP";
            String user = "root";
            String password = "root";

            java.sql.Connection conn = DriverManager.getConnection(url, user, password);
            
            String sql = "INSERT INTO tbl_usuarios (nombre, pass,fk_id_pais,fk_id_ciudad) VALUES (?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            
            stmt.setString(1, nombre);
            stmt.setString(2, pass);
            stmt.setInt(3, id_pais);
            stmt.setInt(4, id_ciudad);

            int rowsInserted = stmt.executeUpdate();
            
            
            
            JOptionPane.showMessageDialog(null, "Registro exitoso");
            
            //Regresar a la ventana de Login
            this.setVisible(false);
            ventanaLogin.setVisible(true);
            
            
            System.out.println("Filas insertadas: " + rowsInserted);

            stmt.close();
            conn.close();
        } catch (ClassNotFoundException | SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAceptarRegistroActionPerformed

    private void cbxPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxPaisActionPerformed
        // TODO add your handling code here:
        
        //Parsear el index del pais a string
        int index_pais = cbxPais.getSelectedIndex();
        String str_id_pais = String.valueOf(index_pais);
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            String url = "jdbc:mysql://localhost:3306/CORPCORP_APP";
            String user = "root";
            String password = "root";

            java.sql.Connection conn = DriverManager.getConnection(url, user, password);
            
            
            String query = "SELECT nombre_ciudad,pk_id_ciudad FROM tbl_ciudades WHERE fk_id_pais = ?";
            
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, str_id_pais);
            ResultSet rs = stmt.executeQuery();
            
            boolean first = true;

            DefaultComboBoxModel<String> modeloCiudades = new DefaultComboBoxModel<>();
            while (rs.next()) {
                modeloCiudades.addElement(rs.getString("nombre_ciudad"));
                if (first) {
                    id_city_rt = rs.getInt("pk_id_ciudad");
                    first = false; 
                }
            }

            // Asignar el modelo al combobox de ciudades
            cbxCiudad.setModel(modeloCiudades);
            
            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_cbxPaisActionPerformed

