    //Insertar lista de paises desde la basde de datos en el comboboxpaises
    private void insertCountries(){
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            String url = "jdbc:mysql://localhost:3306/CORPCORP_APP";
            String user = "root";
            String password = "root";

            java.sql.Connection conn = DriverManager.getConnection(url, user, password);
            
            
            String query = "SELECT nombre_pais FROM tbl_paises";
            PreparedStatement stmt = conn.prepareStatement(query);
            ResultSet rs = stmt.executeQuery();

            DefaultComboBoxModel<String> modelCountries = new DefaultComboBoxModel<>();
            modelCountries.addElement("Seleccione un pais");
            while (rs.next()) {
                modelCountries.addElement(rs.getString("nombre_pais"));
            }

            // Asignar el modelo al combobox de ciudades
            cbxPais.setModel(modelCountries);

            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
